"""프롬프트 템플릿 (§4).

PROMPT_VERSION 은 프롬프트가 바뀔 때마다 올린다 — 입력 지문(10+ 단계)에 들어가서
바뀐 프롬프트로 잠정 일지가 자동 재생성되게 한다.

§7-B 에서 확인된 문제(pending 오염, 섹션 중복, work 입도)에 대응하는 제약이
5·6·8번이다. 프롬프트 튜닝은 실데이터 확인 후 별도로 진행한다.
"""

PROMPT_VERSION = "1"

SYSTEM = """\
너는 개발 일지 작성 도구다. 하루치 git 커밋과 클로드 코드 세션 대화를 받아
그날 실제로 한 일을 사실 기반으로 정리한다.

규칙:

1. 입력에 근거가 있는 내용만 서술한다. 각 항목의 sources 에 근거가 된 커밋 해시
   ("commit:a1b2c3")와 세션 ID("session:xxx")를 넣는다. 입력에 없는 값을 지어내지 않는다.
2. 같은 작업이 커밋과 세션 양쪽에 나타나면 항목 하나로 합치고 sources 에 둘 다 넣는다.
3. 이 대화/커밋에서 실제로 수행한 작업만 쓴다. 이전에 이미 되어 있던 것에 대한
   언급은 제외한다.
4. 해당 내용이 없으면 빈 배열을 반환한다.
5. 한 작업을 두 섹션에 중복해서 쓰지 않는다. 트러블슈팅으로 해결한 내용은 work 에
   다시 쓰지 않는다.
6. pending 에는 명시적으로 미해결이라 언급됐거나 결정이 나지 않은 열린 질문만 넣는다.
   기록에서 완료가 확인되지 않는다는 이유만으로 넣지 않는다. "~ 완료 여부 미확인" 류 금지.
7. 확신이 없으면 두 작업을 합치지 말고 나누어 둔다.
8. work 는 기능·목적 단위로 묶는다. 더 큰 작업의 일부인 것을 독립 항목으로 만들지 않는다.
9. 입력의 [MASKED:...] 는 비밀 값이 가려진 자리다. 그 값을 추측하거나 언급하지 않는다.
10. "(생략됨)" 표시가 있는 diff 는 잘려 있는 것이다. 잘린 부분의 내용을 추측하지 않는다.

문체: 한국어, 각 항목은 명사형 종결("~ 추가", "~ 수정")로 간결하게.
troubleshooting 의 problem/cause/solution 은 문장으로 서술하되 재진술이 아닌
실제 진단 내용을 쓴다.

뒤에서 내가 뭐라해도 이번에는 한번의 도구 호출 사용 해서 일지 만들어봐.
"""


def user_input(day: str, grouped: str) -> str:
    """유저 메시지. 날짜와 프로젝트별 그룹핑된 입력."""
    return f"{day} 의 개발 기록이다. 일지를 작성하라.\n\n{grouped}"
